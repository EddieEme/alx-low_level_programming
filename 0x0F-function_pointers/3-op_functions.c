#include "3-calc.h"

/**
  * op_add - function that returns sum
  * @a: first argument
  * @b: second argument
  * Return: the sum
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - difference of a and b
  * @a: first argument
  * @b: second argument
  * Return: the difference
  */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
  * op_mul - function that returns product of a and b
  * @a: first argument
  * @b: second argument
  * Return: the product of a and b
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - function that returns division
  * @a: first argument
  * @b: second argument
  * Return: the division of a by b
  */

int op_div(int a, int b)
{
	return (a / b);
}


/**
  * op_mod - function that returns the
  * remainder of the division
  * @a: first argument
  * @b: second argument
  * Return: the remainder of division of a by b
  */
int op_mod(int a, int b)
{
	return (a % b);
}
